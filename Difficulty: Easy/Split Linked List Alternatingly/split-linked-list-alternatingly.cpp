class Solution {
    
    public:
    
    vector<Node*> alternatingSplitList(struct Node* head) {
        
        Node *list1Head = head;
        Node *list2Head = head->next;
        Node *list1 = head;
        Node *list2 = head->next;
        while(list1 && list2){
            if(list1->next) list1->next = list1->next->next;
            list1 = list1->next;
            if(list2->next) list2->next = list2->next->next;
            list2 = list2->next;
        }
        return {list1Head, list2Head};
        
    }
};


