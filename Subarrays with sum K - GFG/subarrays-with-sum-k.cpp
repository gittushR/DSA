class Solution{
    public:
    int findSubArraySum(int Arr[], int N, int k)
    {
        // code here
        unordered_map<int,int> prevSum;
        long long sum=0,ans=0;
        for(int i=0;i<N;i++){
            sum+=Arr[i];
            if(sum==k)ans++;
            long long rem=sum-k;
            if(prevSum.find(rem)!=prevSum.end()){
                ans+=prevSum[rem];
            }
            prevSum[sum]++;
        }
        return ans;
    }
};

